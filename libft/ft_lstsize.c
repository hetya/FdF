#include "libft.h"

/**
 * return the size of the list
*/
int	ft_lstsize(t_list *lst)
{
	size_t	i;

	i = 0;
	if (lst)
	{
		while (lst)
		{
			lst = lst->next;
			i++;
		}
		return (i);
	}
	return (0);
}
